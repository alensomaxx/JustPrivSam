class Main{
    public static void main(String args[]){
        double d = 10.0;
        float f=(float)d;
        int i = (int)f;
        
        System.out.println("Before conversion Double value is : "+d);
        System.out.println("After conversion float value is : "+f);
        System.out.println("After conversion Integer value is : "+i);
               
    }
}